package myCPU.core
import spinal.core._

object LA32R {
    def rdRange = 4 downto 0
    def rjRange = 9 downto 5
    def rkRange = 14 downto 10
    def raRange = 19 downto 15

    private def MM(mask: String): MaskedLiteral = {
        val replace_mask = mask.replace(" ", "")
        assert(replace_mask.length == 32)
        MaskedLiteral(replace_mask)
    }

    // 3R-type
    val ADD       = MM("00000000000100000 ----- ----- -----")
    val SUB       = MM("00000000000100010 ----- ----- -----")
    val SLT       = MM("00000000000100100 ----- ----- -----")
    val SLTU      = MM("00000000000100101 ----- ----- -----")
    val NOR       = MM("00000000000101000 ----- ----- -----")
    val AND       = MM("00000000000101001 ----- ----- -----")
    val OR        = MM("00000000000101010 ----- ----- -----")
    val XOR       = MM("00000000000101011 ----- ----- -----")
    val SLL       = MM("00000000000101110 ----- ----- -----")
    val SRL       = MM("00000000000101111 ----- ----- -----")
    val SRA       = MM("00000000000110000 ----- ----- -----")
    
    // 2RI8-type 
    val SLLI      = MM("00000000010000 -------- ----- -----")
    val SRLI      = MM("00000000010001 -------- ----- -----")
    val SRAI      = MM("00000000010010 -------- ----- -----")

    // 2RI12-type
    val SLTI      = MM("0000001000 ------------ ----- -----")
    val SLTUI     = MM("0000001001 ------------ ----- -----")
    val ADDI      = MM("0000001010 ------------ ----- -----")
    val ANDI      = MM("0000001101 ------------ ----- -----")
    val ORI       = MM("0000001110 ------------ ----- -----")
    val XORI      = MM("0000001111 ------------ ----- -----")

    // 2RI16-type
    val LDB       = MM("0010100000 ------------ ----- -----")
    val LDH       = MM("0010100001 ------------ ----- -----")
    val LDW       = MM("0010100010 ------------ ----- -----")
    val STB       = MM("0010100100 ------------ ----- -----")
    val STH       = MM("0010100101 ------------ ----- -----")
    val STW       = MM("0010100110 ------------ ----- -----")
    val LDBU      = MM("0010101000 ------------ ----- -----")
    val LDHU      = MM("0010101001 ------------ ----- -----")

    // 2RI21-type
    val JIRL      = MM("010011 ---------------- ----- -----")
    val BEQ       = MM("010110 ---------------- ----- -----")
    val BNE       = MM("010111 ---------------- ----- -----")
    val BLT       = MM("011000 ---------------- ----- -----")
    val BGE       = MM("011001 ---------------- ----- -----")
    val BLTU      = MM("011010 ---------------- ----- -----")
    val BGEU      = MM("011011 ---------------- ----- -----")

    // 1R20I-type
    val LU12I     = MM("0001010 -------------------- -----")
    val PCADDU12I = MM("0001010 -------------------- -----")

    // I26-type
    val B         = MM("010100 ---------------- ----------")
    val BL        = MM("010101 ---------------- ----------")
}
